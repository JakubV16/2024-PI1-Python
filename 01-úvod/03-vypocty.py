a = int(input("zadaj číslo a: ")) # input je príkazom vstupu. Vypíše na obrazobku text v zátvorke a to čo užívateľ napíše na klávesnici, uloží do premennej a
b = int(input("zadaj číslo b: ")) # input vracia vždy výsledok vo formáte reťazca (postupnosť znakov resp. text), funkcia int() pretipuje reťazec na celé číslo
sucet = a + b
print("Súčet je: ", sucet)
sucin = a * b
print("Súčin je: ", sucin)
rozdiel = a - b
print("Rozdiel je: ", rozdiel)
podiel = a / b 
print("Podiel je: ", podiel)
